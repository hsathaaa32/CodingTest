
public class Person {
	
	public Person(String name, byte age, String address, String city,
			String state, short zip) {
		super();
		this.name = name;
		this.age = age;
		this.address = address;
		this.city = city;
		this.state = state;
		this.zip = zip;
	}

	String name;
	byte age;
	String address;
	String city;
	String state;
	short zip;
}
